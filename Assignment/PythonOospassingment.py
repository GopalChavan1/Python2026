'''5. Write a Python program to create a class representing a binary search tree. Include methods for inserting and searching for elements in the binary tree.'''

class TreeNode:
    def __init__(self, key):
        self.key = key
        self.left = None
        self.right = None


class BinarySearchTree:
    def __init__(self):
        self.root = None

    def insert(self, key):
        """Insert a key into the BST."""
        if self.root is None:
            self.root = TreeNode(key)
        else:
            node = self.root
            while True:
                if key < node.key:
                    if node.left is None:
                        node.left = TreeNode(key)
                        break
                    node = node.left
                elif key > node.key:
                    if node.right is None:
                        node.right = TreeNode(key)
                        break
                    node = node.right
                else:
                    break  # Duplicate, not inserting

    def search(self, key):
        """Search for a key in the BST. Returns True if found."""
        node = self.root
        while node is not None:
            if key == node.key:
                return True
            elif key < node.key:
                node = node.left
            else:
                node = node.right
        return False


