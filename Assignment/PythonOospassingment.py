'''6. Write a Python program to create a class representing a stack data structure. Include methods for pushing and popping elements.'''

class Stack:
    def __init__(self):
        self.items = []

    def push(self, item):
        """Add an item to the top of the stack."""
        self.items.append(item)

    def pop(self):
        """Remove and return the top item from the stack."""
        if not self.is_empty():
            return self.items.pop()
        else:
            raise IndexError("pop from empty stack")

    def peek(self):
        """Return the top item without removing it."""
        if not self.is_empty():
            return self.items[-1]
        else:
            raise IndexError("peek from empty stack")

    def is_empty(self):
        """Check if the stack is empty."""
        return len(self.items) == 0

    def size(self):
        """Return the number of items in the stack."""
        return len(self.items)


# Example usage
s = Stack()
s.push(10)
s.push(20)
s.push(30)

print("Stack size:", s.size())  # Output: 3
print("Top element:", s.peek()) # Output: 30
print("Popped:", s.pop())       # Output: 30
print("Stack size after pop:", s.size())  # Output: 2

